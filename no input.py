# Program to add,subtract,divide and multiply

first_number = int(input("Enter the first number"))
second_number = int(input("Enter the second number"))

sum_number = first_number + second_number
difference_number = first_number - second_number
multiplication_number = first_number * second_number
division_number = first_number / second_number

print (f"the sum of the numbers is {sum_number}")
print (f"the difference of the numbers is {difference_number}")
print (f"the multiplication of the numbers is {multiplication_number}")
print (f"the division of the numbers is {division_number}")
